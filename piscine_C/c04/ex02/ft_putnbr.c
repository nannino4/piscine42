#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	to_be_printed;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb >= 0 && nb < 10)
	{
		to_be_printed = nb + '0';
		write(1, &to_be_printed, 1);
	}
}
