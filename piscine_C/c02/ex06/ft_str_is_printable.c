int		ft_str_is_printable(char *str)
{
	int flag;
	int i;

	i = 0;
	flag = 1;
	while (str[i] != 0)
	{
		if (str[i] < 32)
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
