void	ft_rev_int_tab(int *tab, int size)
{
	int tmp[size];
	int i;

	i = 0;
	while (i < size)
	{
		tmp[i] = tab[i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tmp[size - 1 - i];
		i++;
	}
}
