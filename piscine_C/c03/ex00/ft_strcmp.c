int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int flag;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != 0 && s2[i] != 0)
	{
		i++;
	}
	if (s1[i] < s2[i])
		flag = -1;
	else if (s1[i] > s2[i])
		flag = 1;
	else
		flag = 0;
	return (flag);
}
